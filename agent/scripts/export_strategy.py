# ---- Main --------------------------------------------------------------------

def main():
    parser = argparse.ArgumentParser(description="Export backtest strategy to Pine Script or vnpy")
    parser.add_argument("--run_dir", required=True, help="Backtest run directory (contains config.json + code/signal_engine.py)")
    parser.add_argument("--format", required=True, choices=["pine", "vnpy"], help="Target platform")
    parser.add_argument("--output", required=True, help="Output file path")
    args = parser.parse_args()

    config, source = _load_run(args.run_dir)
    info = _extract_strategy_info(config, source)

    print(f"Strategy: {info['description']}", file=sys.stderr)
    print(f"Symbols: {info['symbols']}", file=sys.stderr)
    print(f"Parameters: {info['parameters']}", file=sys.stderr)
    print(f"Indicators detected: {info['indicators']}", file=sys.stderr)

    if args.format == "pine":
        out = _to_pine(info, source)
    else:
        out = _to_vnpy(info, source)

    Path(args.output).write_text(out, encoding="utf-8")
    print(f"Exported to {args.output} ({len(out)} bytes)")


if __name__ == "__main__":
    main()
